public class BilanganKelipatanDua {
    public static void main(String[] args) {

        System.out.println("           DO__WHILE        ");
        System.out.println("Bilangan Perkalian 2 (1-100)");
        System.out.println("============================");
        int i = 1;
        do{
            System.out.print(i*2 + "   ");
            i ++;
        }while(i<=50);

    }
}
